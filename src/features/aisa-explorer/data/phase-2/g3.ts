    '   — Calibration: predicted probabilities accurately reflect actual rates across groups.',
    '2. For each protected characteristic in scope, define: the subgroup segmentation (how the population is divided), the fairness metric, and the maximum acceptable disparity (e.g., no group may have a false negative rate more than 10% higher than the best-performing group).',
