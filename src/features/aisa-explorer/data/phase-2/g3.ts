  why_it_matters: 'Bias in AI systems reflects and amplifies bias in historical data. A model trained on historical decisions that discriminated against protected groups will predict discriminatory outcomes. Identifying bias sources before training is the only point at which bias can be prevented rather than mitigated after the fact.',
